/* Cancellazione Enumerati */
DROP TYPE IF EXISTS sex;
DROP TYPE IF EXISTS charactertype;
DROP TYPE IF EXISTS relationship;
DROP TYPE IF EXISTS usertype;
DROP TYPE IF EXISTS subtype;
DROP TYPE IF EXISTS rentaltype;
DROP TYPE IF EXISTS staytype;

/* Creazione enumerati */
CREATE TYPE sex AS ENUM ('M','F','N');
CREATE TYPE charactertype AS ENUM ('PG','PNG');
CREATE TYPE relationship AS ENUM('Amicizia','Inimicizia','Familiare','Lavorativo','Amoroso');
CREATE TYPE usertype as ENUM('G','S');
CREATE TYPE subtype as ENUM('I','R','A');
CREATE TYPE rentaltype as ENUM('00','01','A0','B0','C0','A1','B1','C1');
CREATE TYPE staytype as ENUM('MP','CP','BB');

/* Creazione tabelle */
CREATE TABLE IF NOT EXISTS Storia(
	titolo varchar (50) primary key not null,
	trama varchar(255) not null
);
CREATE TABLE IF NOT EXISTS LARP(
	codice varchar(6) primary key not null,
	storia varchar(50) not null,
	titolo varchar(50) not null,
	capitolo int not null,
	sceneggiatura varchar(255) not null
);
CREATE TABLE IF NOT EXISTS Personaggio(
	codice varchar(6) primary key not null,
	nome varchar(50) not null,
	cognome varchar(50) not null,
	pseudonimo varchar(50),
	sesso sex not null,
	professione varchar(100) not null,
	tipo charactertype not null
);
CREATE TABLE IF NOT EXISTS Relazione(
	personaggio1 varchar(6) not null,
	personaggio2 varchar(6) not null,
	tipo relationship not null,
	note varchar(255) not null,
	primary key(personaggio1, personaggio2),
	foreign key(personaggio1) references Personaggio(codice),
	foreign key(personaggio2) references Personaggio(codice)
);
CREATE TABLE IF NOT EXISTS Ruolo(
	larp varchar(6) not null,
	personaggio varchar(6) not null,
	primary key(larp, personaggio),
	foreign key(larp) references LARP(codice),
	foreign key(personaggio) references Personaggio(codice)
);
CREATE TABLE IF NOT EXISTS Luogo(
	id serial not null primary key,
	nome varchar (50) not null,
	via varchar(50) not null,
	civico smallint not null,
	citta varchar(50) not null,
	cap smallint not null,
	provincia char(2) not null
);
CREATE TABLE IF NOT EXISTS Utente(
	cf varchar(16) not null primary key,
	nome varchar(50) not null,
	cognome varchar(50) not null,
	email varchar(255) not null,
	telefono varchar(18) not null,
	primosoccorso boolean not null,
	tipo usertype not null,
	admin boolean
);
CREATE TABLE IF NOT EXISTS Noleggio(
	id serial not null primary key,
	scelta rentaltype not null,
	importo money not null
);
CREATE TABLE IF NOT EXISTS Pernottamento(
	id serial not null primary key,
	opzione staytype not null,
	importo money not null
);
CREATE TABLE IF NOT EXISTS Iscrizione(
	tipologia subtype primary key not null,
	importo money not null
);
CREATE TABLE IF NOT EXISTS Evento(
	codice varchar(10) primary key not null,
	larp varchar(6) not null,
	luogo int not null,
	datainizio date not null,
	datafine date not null,
	foreign key(larp) references LARP(codice),
	foreign key(luogo) references Luogo(id)
);
CREATE TABLE IF NOT EXISTS Prenotazione(
	evento varchar(9) not null,
	personaggio varchar(6) not null,
	utente varchar(16) not null,
	dataprenotazione date not null,
	importo money not null,
	iscrizione subtype not null,
	noleggio int,
	pernottamento int,
	primary key(evento, personaggio),
	foreign key(evento) references Evento(codice),
	foreign key(personaggio) references Personaggio(codice),
	foreign key(utente) references Utente(cf),
	foreign key(iscrizione) references Iscrizione(tipologia),
	foreign key(noleggio) references Noleggio(id),
	foreign key(pernottamento) references Pernottamento(id)
);
CREATE TABLE IF NOT EXISTS Assegnazione(
	evento varchar(9) not null,
	personaggio varchar(6) not null,
	utente varchar(16) not null,
	primary key(evento, personaggio),
	foreign key(evento) references Evento(codice),
	foreign key(personaggio) references Personaggio(codice),
	foreign key(utente) references Utente(cf)
);

